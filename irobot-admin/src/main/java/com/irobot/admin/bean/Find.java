package com.irobot.admin.bean;

public class Find {

    private String setNumber;

    private String noRecords;

    private String noEntries;

    private String sessionId;

    public String getSetNumber() {
        return setNumber;
    }

    public void setSetNumber(String setNumber) {
        this.setNumber = setNumber;
    }

    public String getNoRecords() {
        return noRecords;
    }

    public void setNoRecords(String noRecords) {
        this.noRecords = noRecords;
    }

    public String getNoEntries() {
        return noEntries;
    }

    public void setNoEntries(String noEntries) {
        this.noEntries = noEntries;
    }

    public String getSessionId() {
        return sessionId;
    }

    public void setSessionId(String sessionId) {
        this.sessionId = sessionId;
    }
}
